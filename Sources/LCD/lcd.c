//display a col*8 picture,col:high 1 bit mean reverse or not,low 7 bits mean the column numbers
void dis_pic_8(byte col,byte page,byte column,const byte *pic)
{
 byte m,n,reverse;
 n=col&0x7F;
 reverse=col&0x80;
 CS=0;
 lcd_add(page,column);
 if(reverse)
  {
   for (m=0;m<n;m++)
    {
     dat(~*pic);
     pic++;
    }
  }
 else
  {    
   for (m=0;m<n;m++)
    {
     dat(*pic);
     pic++;
   }
  }
 CS=1;
}
